<h2>今回の3選に関してサークルメンバーから</h2>
<p>
　音楽の好みについて話すことはさることながら、同人音楽の好みを語り合うって機会ってなかなか無いように思います。今回、企画を通じて紹介されている作品は僕自身知らないものが多くてとても新鮮ですし、何より選んだ人の好みや気持ちが伝わってくるような気がしました。選ばれる作品も殆ど被っていないし、リスナーの数だけやはり聴き方があるのだなあと当たり前の事実に改めて気付かされました。<br>
　「あ、これって自分も持ってる！」「実は聴いたことないんだけど聴いてみようかな」等々。こんな思いを持ったり、自分の知らない作品との出会いの場にもなると思っていますし、事実僕はそう思っているわけでして…。この企画が、どんどん聴いてどんどん発信するきっかけにならんことを！(椎名)
</p>
<br>
<p>
　まず、今回の企画を最初に考え、同好会の企画として快諾していただいたkizkizさんに感謝を。そして、参加していただいた19名の方々に感謝を。これだけの多くの方に参加して頂いて、正直驚いています。自分でも知らないサークルがまだまだあると思い知らされると同時に、物凄く新鮮味もあり、興味をそそられる作品ばかりでした。<br>
　それぞれの趣味嗜好もありますが、知らないサークル・作品やジャンルに触れる、少しでも良いきっかけになったのではないかと感じています。この企画を通じて、各々で発信したり、共有する面白さが広まれば嬉しいです。(マブ)
</p>
<br>
<p>
　正直、内輪な人だけしか集まらないかもしれない、と思っていたので、思ったより多くの方々にご参加いただけました。また聴き専だけでなく、作り手の方にもご参加いただきました。我々の知名度の点もあり、ジャンル的な偏り等はどうしても出てきてはいますが、今後「書くこと」も広がり、知らなかったＣＤだけでなく、知らなかったジャンルへのアプローチともなって行ければ嬉しいです。また、嗜好の合う方との繋がるサポートとなれば嬉しいです。（みやわ）
</p>


<h2>あとがき</h2>
<p>　今回は準備がままならずとても雑多にオススメ3選とさせていただきましたが、個々人が選ぶ上で、本当に大好きな３つでなくとも、たとえば新規サークルで知られていないから敢えてオススメしたいという形であったり、オリジナルから１つ・東方から１つ・ボカロから１つなどという選び方といった別の選び方でも面白いかなと思っています。<br>
　今後も定期企画として行っていく予定ですが、今回のような「最近のイベントのCD」という括りで無い、もっと古いものを取り上げることも含めて、レビューとまではいかなくても、なにか良いものを伝えるというのが、いろいろな場所で行われれば嬉しいです。
</p>

