<h2>影響を受けたアーティスト・作品！</h2>
<p>
――影響を受けたアーティストや作品は？<br />
<strong>未</strong>　まず、マミュッカ全体で言うとディズニーが好きなんです。台詞が入ってる音楽っていうとみんな一括りにされがちなんですけど、それに対して「違うよ、ディズニーなんだよ」っていう風に言いたい。<br />
<strong>ま</strong>　みんなそういう音楽が好きだからこそ、感性が似てるのもあるんだなあとは…。ティム・バートンの映画みたいな。<br />
<strong>未</strong>　私も大好きなんですよね。ここで「まじで？」っていうようなものなんですが、我々からすると面白い答えをオッカからもらってます。すごくいい答えが返ってきてるんですけど、「影響を受けたアーティスト」は、Danny Elfman（映画『Nightmare before Christmas』の作曲・声の出演）とThe Beatlesだそうです。<br />
<strong>未・ま</strong>　（笑。<br />
――Danny Elfmanはマミュッカさんのサウンドのイメージに近いような気がします<br />
<strong>未</strong>　あれのパクりだって言われるんだったら本望です（笑。これ読むの恥ずかしいんだけど…。<br />
<strong>ま</strong>　「Danny Elfmanには絵に合った音作り、独創性というラインで大変衝撃を受けた記憶があります。The Beatlesに関しては曲自体が素晴らしいのももちろん、歌うことの楽しさや自分たちのやりたいことを表現しているところや聴いている人を巻き込むような音楽にひどく感銘を受け、曲を作る上での根底の意識につながっています」<br />
<strong>未</strong>　というオッカの文章を見て私たちは初めて、「ビートルズ好きなんだ?!」ってなって（笑。私もビートルズは大好きなんですけど…。<br />
<strong>ま</strong>　しかも影響を受けたんだっていう。面白かったんだよね。こんな真面目な文章で返ってくるとは思わなくて（笑。めうたんは？　小説書いてたときの影響受けた人とか…どういう人が好きだったとか。<br />
<strong>未</strong>　私は、作詞の影響を受けた人はたぶんいないと思うんです。英語のディズニーとか、『Nightmare before Christmas』の原語の英語のフィーリングをクサくならないようにしてはいます。自分が読んでも笑わないような、クサくなりすぎないっていうのだけはずっと意識してて。あと、オッカがどこかに書いてくれてるんですけど、「子供向けの目線でいきたい」っていうのがマミュッカ全体としてありますね。同人音楽のリスナーに子供はいないと思うので本当に子供向けかと言われるとそうではないと思うんですけど。<br />
<strong>ま</strong>　誰が見ても理解できるような根本的な楽しさというか。「読み解くのが楽しい音楽が好き」って人がいますよね。私はそういうのが得意ではないですけど…。<br />
<strong>未</strong>　もっと気楽に行きたいなっていうのがマミュッカの方向性で、そういうヘラヘラな感じかな？　重くなりすぎないような。<br />
――肩に力を入れないでできるような音楽？<br />
<strong>未</strong>　そうです。そういう雰囲気だけディズニーとかからもらってるかなあ。でもあまり直接影響受けたって人はいないです。<br />
――まめこさんが影響を受けたアーティストや作品は？<br />
<strong>ま</strong>　私も直接の影響はないですね。いろんなジャンルの曲が好きでいろんな歌い方をしたくて、ロックにハマってた時期もあり、ミュージカル音楽も好きだったりという感じです。私もディズニーの映画が好きで、数え切れないくらい観た作品もあるくらい物心ついたときから観てたんです。ディズニーはミュージカル女優さんが声をあてたりしているのを見てていろいろやりたいっていう気持ちと、演技を元々やってたのでマミュッカみたいな音楽では普通の歌い方をしたんじゃ物足りないなという気持ちがありました。歌詞が困った内容だったらその感情を入れてないと、どうしても自分の中で「物足りない、感情が入ってない」って思っちゃうので、曲に合った歌い方をしようって思ってたら今のスタイルができたので、特に歌い方の影響はないと思います。ただ、最近になってかっこいい歌が歌いたくなったけど今まで歌ったことないし、ロックの歌い方ってどういう歌い方をすればロックになるんだろうって思って、B’zを聴いたりしてます。<br />
<strong>未</strong>　B'zなの？（笑<br />
<strong>ま</strong>　B'zはちょっと独特だけど（苦笑。ロックっぽい歌い方の特徴を探るっていう意味ではロックを聴いて勉強したり、こういう部分が入ってるとロックっぽいなっていうのはあります。どうしたらこういう風になるんだろうっていうのは考えて聴いてます。<br />
――自分の歌いたい音楽の歌い方を吸収していく感じでしょうか？<br />
<strong>ま</strong>　そうですね。声だけの問題じゃないなとは思ってます。歌い方で変えられるって思ったので。<br />
<strong>未</strong>　今の話を要約するとすれば、オッカがDanny ElfmanとThe Beatlesに影響を受けていたというところできれいに話がまとまりましたね！ 私もビートルズ大好きだよ。<br />
<strong>ま</strong>　私も好きだけどね（笑。<br />
――やはりディズニーの影響が根底に？<br />
<strong>未</strong>　そうですね。<br />
<strong>ま</strong>　でも目指したというよりはディズニーを観たときに感じるような感覚をリスナーの人に持ってもらえるような作品がいいなという感じですね。<br />
<strong>未</strong>　いいこと言ったね！
</p>
