<br />
「<a href="http://circle.dojin-music.info/2011au/">同人音楽.book -2011 Autumn-</a>」にて、Mamyukkaの”制作工場長”未羽さんと”ご連絡班長”まめこさんのお2人にインタビューさせていただきました。<br />
ここでは<a href="http://mamyukka.tank.jp/">Mamyukka</a>さまのネット公開曲「チョコっと★ましゅまるん」についてのお話、メンバーが影響を受けた音楽・アーティストについてのお話を掲載！　ここでしか読めない記事なので、どうかお読み忘れのなきよう！<br />
<br />
<h2>チョコっと★ましゅまるん！</h2>
<p>
<script type="text/javascript" src="http://ext.nicovideo.jp/thumb_watch/sm9708142?w=490&h=307"></script><noscript><a href="http://www.nicovideo.jp/watch/sm9708142">【ニコニコ動画】【バレンタイン】チョコ★っとましゅまるん【オリジナル曲＆PV】</a></noscript>

<br />
――この曲はマミュッカさんの中で唯一の電波ソングだと思うのですが<br />
<strong>まめこ</strong>（以下、<strong>ま</strong>）　『Evilz』のアンケートが作るきっかけだったんです。<br />
<strong>未羽</strong>（以下、<strong>未</strong>）　アンケート項目に「マミュッカにどんな曲を作ってほしいですか」っていうのがあって、ファンタジーとか和風とか多くの選択肢がある中、「一応入れとく？」という感じで入れた「電波曲」という項目に一つだけチェックを入れて、アンケートを出してくれた人がいたんです。これを見た瞬間に「これは相当望まれてる。このアンケートから強い意志を感じる。これはやるしかない！」って決めたのが2009年11月くらいです。設定はすごい勢いでできたんです。「猫耳がいい！」みたいなことを私が言って、オッカは「私ウサギ！」って言って、まめちゃんがいない間だったのに恐ろしいスピードでした。そのあとまめちゃんが見て「こいつら何やってんだ」という感じの冷めた態度でポカーンとしてて、「やりたければやれば？」っていう感じでした（笑。<br />
<strong>ま</strong>　確かにほとんどの設定が出来てて「お前はうさぎ役な？」とまで言われて「あ、はい…」みたいな感じだった（笑。<br />
<strong>未</strong>　映像を作ってくれてるろんずさんとまめちゃんが知り合いだったので動画製作をお願いしました。「どこまで萌え？あくまで健全」「見たことのないマミュッカ。マミュッカの本気」って過去のメールに書いてある（笑。絶対オッカだよこれ（笑。<br />
<strong>ま</strong>　絵師さんも探して「この絵かわいいね」っていうことでお願いしたら、ろんずくんがかなり本気になってくれて、どうせならみんなでファイル共有ソフトを使って絵師さんも含めてやりとりしようってなったんです。ろんずくんがアニメーションの設定資料をかなり細かく書いてくれて、それを絵師さんに見せたら絵師さんもいい人で描いてくれるのがすごく早かったんです。早い上に表情のパターンをいろいろ挙げてくれて。<br />
<strong>未</strong>　あれよあれよと動画になっていったんです。最初は歌詞をのせて動画では少し動いてくれればいいって考えていて、簡単なものをお願いしますってろんずくんにお願いしたら、彼が本気で返してきて…っていう流れであんなことになっちゃったんだろうな（笑。これは歌詞を2人で書いたんです。<br />
<strong>ま</strong>　新宿でカラオケに入って2人でずっと歌詞を書きながら。<br />
<strong>未</strong>　あんなに2人でやったのは初めて、かつアレだけだね。<br />
<strong>ま</strong>　結構さらさらできたんだよ。<br />
<strong>未</strong>　「ましゅまるん」ってなんだろうねって言いながらね（笑。<br />
<strong>ま</strong>　「曖昧もこもこ」とかノリで作っちゃったから（笑。キャラクター設定を考えてみようってなったときにオッカが美少女ゲームの紹介文のようなものを挙げて、「こいつを攻略するのは難しいぞ」とか難易度も☆でわざわざ書いてたりとか。<br />
<strong>未</strong>　サイトに書いてある説明の元の文章をノリノリで書いて送ってきたんです（笑。<br />
<strong>ま</strong>　「もういいんじゃないこれで。これ以上面白いのは書けないよ」ってなって採用に。<br />
<strong>未</strong>　あれはきっとオッカが相当乗り気だったからこそできた作品ですね。<br />
<strong>ま</strong>　そのファイル共有ソフトが、ファイルが送られるとディスプレイの下の方にポップアップが出てくるんですけど、それが鳴る度に「またなんかきた！」って。<br />
<strong>未</strong>　そのポップが出る頻度が半端じゃなくて、「またきた！」ってなってました。<br />
<strong>ま</strong>　「今度はなんだろう？」って楽しんでました。<br />
<strong>未</strong>　楽しかったね。「ましゅまるん」は今までやったことがなかった制作だったので話すことがいっぱいあるね。<br />
<strong>ま</strong>　ろんずくんと絵師さんの力が相当大きくて、みんな熱かったよね。<br />
<strong>未</strong>　熱いバレンタインだった（笑。でも周りに「なんで!?」って言われたのがすごくうれしかった。<br />
――電波曲というのが唐突過ぎたからでしょうか？<br />
<strong>未</strong>　「どうしてこうなっちゃったの!?」みたいな感想が。<br />
<strong>ま</strong>　この曲でもやっぱり「オッカすごいじゃん」って思ったよね。<br />
<strong>未</strong>　「これもオッカさんなの？」って言われて「そうだぜ」という感じでした。<br />
<strong>ま</strong>　毎回そんな風に思っちゃうご自愛サークルです（笑。<br />
